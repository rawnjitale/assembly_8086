TITLE TO READ STRING AND DISPLAY IN DIAGONAL
.MODEL SMALL
.STACK 200H
.DATA
PARA LABEL BYTE
LEN DB 40
ACT DB ?
STR DB 40 DUP("$")
MSG DB "ENTER A STRING: ",13,10,"$"
nl db 13,10,13,10,"$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV AH,09H
LEA DX,MSG
INT 21H
MOV AH,0AH
LEA DX,PARA
INT 21H
; to clear the screen and display
MOV AH,00H
MOV AL,03H
INT 10H
MOV CH,0
MOV CL,0
LEA SI,STR
BACK:
MOV AH,02H
MOV DH,CH
MOV DL,CL
INT 10H
MOV AH,02H
MOV DL,[SI]
INT 21H
INC SI
INC CL
CMP BYTE PTR [SI],' '
JE NEXT_LINE
CMP BYTE PTR [SI],'$'
JE END_PROGRAM
JMP BACK
NEXT_LINE:
INC CH
JMP BACK
END_PROGRAM:
MOV AH,4CH
INT 21H
MAIN ENDP
END
